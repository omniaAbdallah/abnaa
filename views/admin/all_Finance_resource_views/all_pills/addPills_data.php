            var val = $("#value").val();
            var band1_value = $(".band1_value").val();
            var band2_value = $(".band2_value").val();
            if (band1_value) {
                band1_value = parseInt(band1_value);
            } else {
                band1_value = 0;
            }
            if (band2_value) {
                band2_value = parseInt(band2_value);
            } else {
                band2_value = 0;
            }
            if (val != (band2_value + band1_value) && ((band2_value + band1_value) > 0)) {
                alert('  المبلغ المتاح لا يساوي مجموع قيم البنود');
                $(".value").val(' ');
            }
        }

        function ConvertToDecimal(num) {
            var n = parseFloat(num);
            if (isInt(n)) {
                return num;
            } else {
                return num.toFixed(2);
            }
